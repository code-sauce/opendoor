class Listing():
    def __init__(self, listing_id, street, status, price, bedrooms, bathrooms, sq_ft, longitude, latitude):
            self.id = listing_id
            self.street = street
            self.status = status
            self.price = price
            self.bedrooms = bedrooms
            self.bathrooms = bathrooms
            self.sq_ft = sq_ft
            self.longitude = longitude
            self.latitude = latitude
